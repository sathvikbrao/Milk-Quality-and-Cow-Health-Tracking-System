    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

        if (jDateChooser1.getDate() == null) {
            JOptionPane.showMessageDialog(null, "Enter the date");
        } else {
            try {
                Connection connection = getNewConnection();

                PreparedStatement checkPS = connection.prepareStatement("SELECT Report_Id FROM Reportdate WHERE Report_Date = ? and Report_Id in (SELECT Report_Id FROM Report WHERE User_Id = ?)");
                checkPS.setDate(1, new java.sql.Date(jDateChooser1.getDate().getTime()));

                checkPS.setString(2, userId);

                ResultSet checkRS = checkPS.executeQuery();

                if (checkRS.next()) {
                    String reportId = checkRS.getString("Report_Id");
                    jTextField4.setText(reportId);

                    // fetch user id, doctor id, fat, snf, clr from Report table
                    PreparedStatement fetchPS = connection.prepareStatement("SELECT User_Id, Fat, SNF, CLR FROM Report WHERE Report_Id = ? and user_id = ?");
                    fetchPS.setString(1, reportId);
                    fetchPS.setString(2, userId);
                    System.out.println(userId);

                    ResultSet fetchRS = fetchPS.executeQuery();

                    if (fetchRS.next()) {

                        userId = fetchRS.getString("User_Id");

                        double fat = fetchRS.getDouble("Fat");
                        double snf = fetchRS.getDouble("SNF");
                        double clr = fetchRS.getDouble("CLR");
                        jTextField2.setText(userId);
                        jTextField6.setText(String.valueOf(fat));
                        jTextField7.setText(String.valueOf(snf));
                        jTextField8.setText(String.valueOf(clr));
                    }

                    // fetch doctor id from Reportdate table
                    PreparedStatement fetchDoctorPS = connection.prepareStatement("SELECT Doctor_Id FROM Reportdate WHERE Report_Id = ?");
                    fetchDoctorPS.setString(1, reportId);
                    ResultSet fetchDoctorRS = fetchDoctorPS.executeQuery();
                    if (fetchDoctorRS.next()) {
                        String doctorId = fetchDoctorRS.getString("Doctor_Id");
                        jTextField5.setText(doctorId);
                    }
                    // fetch user name, local loc from Dairyfarmer table
                    PreparedStatement fetchUserPS = connection.prepareStatement("SELECT User_Name, Local_Loc FROM Dairyfarmer WHERE User_Id = ?");
                    fetchUserPS.setString(1, userId);
                    ResultSet fetchUserRS = fetchUserPS.executeQuery();
                    if (fetchUserRS.next()) {
                        String userName = fetchUserRS.getString("User_Name");
                        String localLoc = fetchUserRS.getString("Local_Loc");
                        jTextField3.setText(userName);
                    }
                    // fetch manu id, item no from Suggestion table
                    PreparedStatement fetchSuggestionPS = connection.prepareStatement("SELECT Manu_Id, Item_No FROM Suggestion WHERE Report_Id = ?");
                    fetchSuggestionPS.setString(1, reportId);
                    System.out.println(reportId);
                    ResultSet fetchSuggestionRS = fetchSuggestionPS.executeQuery();
                    if (fetchSuggestionRS.next()) {
                        String manuId = fetchSuggestionRS.getString("Manu_Id");
                        System.out.println(manuId);
                        String itemNo = fetchSuggestionRS.getString("Item_No");
                        jTextField9.setText(manuId);
                        jTextField10.setText(itemNo);
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "Report Id on this date does not exist");
                    jTextField2.setText("");
                    jTextField3.setText("");
                    jTextField4.setText("");
                    jTextField5.setText("");
                    jTextField6.setText("");
                    jTextField7.setText("");
                    jTextField8.setText("");
                    jTextField9.setText("");
                    jTextField10.setText("");
                }
            } catch (SQLException e) {
//                JOptionPane.showMessageDialog(null, "Report Id on this date does not exist");
                e.printStackTrace();
            }

        }
    }//GEN-LAST:event_jButton3ActionPerformed

