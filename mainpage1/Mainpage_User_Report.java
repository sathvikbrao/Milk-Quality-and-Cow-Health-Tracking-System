    private void jLabel16MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel16MouseClicked
        String date = ((JTextField)jDateChooser1.getDateEditor().getUiComponent()).getText();                 
        jPanel4.setVisible(true);
    String userId = jTextField2.getText();
    String username = jTextField3.getText();
    String reportId = jTextField4.getText();
    String doctorId = jTextField5.getText();
    String fat = jTextField6.getText();
    String snf = jTextField7.getText();
    String clr = jTextField8.getText();
    String manufacturerId = jTextField9.getText();
    String itemNo = jTextField10.getText();
     jPanel4.setVisible(true);
    DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
    if (!date.isEmpty() && !userId.isEmpty() && !username.isEmpty() && !reportId.isEmpty() && !doctorId.isEmpty() && !fat.isEmpty() && !snf.isEmpty() && !clr.isEmpty() && !manufacturerId.isEmpty() && !itemNo.isEmpty()) {
        model.setRowCount(0);
        model.addRow(new Object[]{"MILK QUALITY REPORT :"});
        model.addRow(new Object[]{"Date :", date});
        model.addRow(new Object[]{"User Id :", userId});
        model.addRow(new Object[]{"Username :", username});
        model.addRow(new Object[]{"Report Id :", reportId});
        model.addRow(new Object[]{"Doctor Id :", doctorId});
        model.addRow(new Object[]{"Fat :", fat});
        model.addRow(new Object[]{"SNF :", snf});
        model.addRow(new Object[]{"CLR :", clr});
        model.addRow(new Object[]{"Manufacturer Id :", manufacturerId});
        model.addRow(new Object[]{"Item No :", itemNo});
        try {
    boolean complete = jTable2.print();
     
    if (complete) {
        
        JOptionPane.showMessageDialog(null, "Print Success, Downloading the file.");
         jPanel4.setVisible(false);
        
    } else {
         
        JOptionPane.showMessageDialog(null, "Print Cancelled.");
        jPanel4.setVisible(false);
        
    }
} catch (PrinterException pe) {
    JOptionPane.showMessageDialog(null, "Print Exception Occurred.");
    jPanel4.setVisible(false);
    
}

    } else {
        JOptionPane.showMessageDialog(null, "Fields must be filled to generate the report.");
        jPanel4.setVisible(false);
    }
    
    }//GEN-LAST:event_jLabel16MouseClicked


